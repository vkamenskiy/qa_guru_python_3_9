
class User:
    def __init__(
        self,
        name,
        last_name,
        email,
        gender,
        mobile_number,
        date_of_birth,
        subjects,
        hobbies,
        picture,
        current_address,
        state,
        city,
    ):
        self.name = name
        self.last_name = last_name
        self.email = email
        self.gender = gender
        self.mobile_number = mobile_number
        self.date_of_birth = date_of_birth
        self.subjects = subjects
        self.hobbies = hobbies
        self.picture = picture
        self.current_address = current_address
        self.state = state
        self.city = city
